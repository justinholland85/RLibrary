Lib.GiveNames  <-  function(X, Names){
  
  names(X)   <-  Names
  return(X)
  
}

#####################################################################################################

Lib.GiveNames.Rows  <-  function(X, Names){
  
  rownames(X)  <-  Names
  return(X)
  
}

#####################################################################################################

Lib.GiveNames.Cols  <-  function(X, Names){
  
  colnames(X)  <-  Names
  return(X)
  
}

